#include <SFML/Graphics.hpp>

namespace Arc
{
	////////////////////////////////////////////////////////////
	/// \brief Holds a game configuration
	////////////////////////////////////////////////////////////
	class GameConfig
	{
	public:
		enum Language
		{
			English
		};

		////////////////////////////////////////////////////////////
		/// \brief Default constructor.
		////////////////////////////////////////////////////////////
								GameConfig();

		////////////////////////////////////////////////////////////
		/// \brief Setup constructor.
		/// \param vMode The sf::VideoMode.
		/// \param wTitle The title of the window.
		/// \param wStyle The style of the window.
		/// \param wFPS The max FPS of the window.
		/// \param wVSync Enable or disable v-sync.
		/// \param lang The language to use for all strings.
		////////////////////////////////////////////////////////////
								GameConfig(sf::VideoMode vMode, sf::String wTitle,
										   int wStyle, int wFPS, bool wVSync, Language lang);

		////////////////////////////////////////////////////////////
		/// \brief Default destructor
		////////////////////////////////////////////////////////////
								~GameConfig();

		////////////////////////////////////////////////////////////
		/// \brief Sets the Language.
		/// \param language The language to use.
		////////////////////////////////////////////////////////////    
		void                    setLanguage(const Language& language);

		////////////////////////////////////////////////////////////
		/// \brief Sets the max window FPS.
		/// \param winFPS the window FPS.
		////////////////////////////////////////////////////////////   
		void                    setWinFPS(int winFPS);

		////////////////////////////////////////////////////////////
		/// \brief Sets the max window FPS.
		/// \param winFPS the window FPS.
		////////////////////////////////////////////////////////////   
		void                    setVidMode(const sf::VideoMode& winMode);

		////////////////////////////////////////////////////////////
		/// \brief Sets the window sf::Style.
		/// \param winStyle The style to change to.
		////////////////////////////////////////////////////////////   
		void                    setWinStyle(int winStyle);

		////////////////////////////////////////////////////////////
		/// \brief Sets the title of the window.
		/// \param winTitle The title to use.
		////////////////////////////////////////////////////////////   
		void                    setWinTitle(const sf::String& winTitle);

		////////////////////////////////////////////////////////////
		/// \brief Sets if the window should use v-sync.
		/// \param winVSync The v-sync boolean.
		////////////////////////////////////////////////////////////     
		void                    setWinVSync(bool winVSync);

		////////////////////////////////////////////////////////////
		/// \brief Gets the language.
		/// \return The language.
		////////////////////////////////////////////////////////////  
		const Language&         getLanguage()   const;

		////////////////////////////////////////////////////////////
		/// \brief Gets the FPS.
		/// \return The FPS.
		////////////////////////////////////////////////////////////  
		int                     getWinFPS()     const;

		////////////////////////////////////////////////////////////
		/// \brief Gets the sf::VideoMode.
		/// \return The sf::VideoMode.
		////////////////////////////////////////////////////////////  
		const sf::VideoMode&    getVidMode()    const;

		////////////////////////////////////////////////////////////
		/// \brief Gets the sf::Style.
		/// \return The sf::Style..
		////////////////////////////////////////////////////////////  
		int                     getWinStyle()   const;

		////////////////////////////////////////////////////////////
		/// \brief Gets the window title.
		/// \return The window title.
		////////////////////////////////////////////////////////////  
		const sf::String&       getWinTitle()   const;

		////////////////////////////////////////////////////////////
		/// \brief Gets the v-sync boolean.
		/// \return The v-sync boolean.
		////////////////////////////////////////////////////////////  
		bool                    isWinVSync()    const;

		////////////////////////////////////////////////////////////
		/// \brief Gets the window size.
		/// \return The window size.
		////////////////////////////////////////////////////////////  
		sf::Vector2f            getWinSize()    const;


	private:
		////////////////////////////////////////////////////////////
		// Member data.
		////////////////////////////////////////////////////////////
		sf::VideoMode           mVidMode;
		sf::String              mWinTitle;
		int                     mWinStyle, mWinFPS;
		bool                    mWinVSync;
		Language                mLanguage;
	};
}